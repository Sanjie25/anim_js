function inputs() {
  if (keys['ArrowUp']) {
    player1.vy = -5;
  }

  if (keys['ArrowDown']) {
    player1.vy = 5;
  }

  if (keys['Space']) {
    const bullet = player1.fire();
    if (bullet) {
      playerBullets.push(bullet);
    }
  }
}

