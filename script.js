// -------------- PIXEL -----------------------------------------
class Pixel {
  constructor(x, y, size, color = "#fff") {
    this.x = x;
    this.y = y;
    this.size = size;
    this.color = color;
    this.vx = 0;
    this.vy = 0;
    this.gravity = 0.5;
    this.life = 180;
  }

  update() {
    this.x += this.vx;
    this.y += this.vy;

    if (this.vx != 0 || this.vy != 0) {
      this.vy += this.gravity;
      this.life--;
    }
  }

  break() {
    this.vx = (Math.random() + 1) * 10;
    this.vy = (Math.random() + 1) * 10;
    this.color = '#ff0000';
  }

  draw(ctx) {
    if (this.life > 0) {
      ctx.fillStyle = this.color;
      ctx.fillRect(this.x, this.y, this.size, this.size);
    }
  }

  alive() {
    return this.life > 0;
  }
}
// ------------------ PIXEL END --------------------------------

// --------------------- WORD -----------------------------------
class Word {
  constructor(x, y, word, alive = true, pixels = [], size = 10) {
    this.x = x;
    this.y = y;
    this.word = word;
    this.alive = alive;
    this.pixels = pixels;
    this.size = size;
    this.alive = true;
    this.lastFireTime = 0;
    this.fireRate = 2000;

    this.getPixels();
  }


  getPixels() {
    for (let i = 0; i < this.word.length; i++) {
      let grid = data[this.word[i]];

      grid.forEach((row, j) => {
        row.forEach((column, k) => {
          if (column == 1) {
            let pic = new Pixel(this.x + i * 80 + k * this.size, this.y + j * this.size, this.size);
            this.pixels.push(pic);
          }
        });
      });
    }
  }

  update() {
    this.pixels.forEach((pixel) => pixel.update());

    this.pixels = this.pixels.filter(pixel => pixel.alive());

    if (this.pixels.length == 0) {
      this.alive = false;
    }
  }

  draw(ctx) {
    // console.log(this.pixels);
    for (let index = 0; index < this.pixels.length; index++) {
      const element = this.pixels[index];
      element.draw(ctx);
    }
  }
}
