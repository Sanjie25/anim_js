class Bullet {
  constructor(x, y, vx, vy, color = "#ffff00") {
    this.x = x;
    this.y = y;
    this.vx = vx;
    this.vy = vy;
    this.color = color;
    this.size = 4;
    this.alive = true;
  }

  update() {
    this.x += this.vx;
    this.y += this.vy;

    if (this.x < 0 || this.x > canvas.width || this.y < 0 || this.y > canvas.height) {
      this.alive = false;
    }
  }

  draw(ctx) {
    if (this.alive) {
      ctx.fillStyle = this.color;
      ctx.fillRect(this.x, this.y, this.size, this.size);
    }
  }
}

class Player {
  constructor(x, y, vx, vy, health = 10, power = "weakling") {
    this.x = x;
    this.y = y;
    this.health = health;
    this.vx = vx;
    this.vy = vy;
    this.size = 30;
    this.health = health;
    this.power = power;
    this.alive = true;
    this.lastFireTime = 0;
    this.fireRate = 200;

    // defining the path;
    this.path = new Path2D();

  }

  update() {
    this.x += this.vx;
    this.y += this.vy;

    this.vx = 0;
    this.vy = 0;
  }

  draw(ctx) {
    if (!this.alive) return;
    ctx.save();
    // simple triangle ship
    ctx.beginPath();
    ctx.arc(this.x + this.size, this.y + this.size, 30, 0, 2 * Math.PI, false);
    ctx.moveTo(this.x + 40, this.y + 50);
    ctx.lineTo(this.x + 40, this.y + 10);
    ctx.lineTo(this.x + 80, this.y + 30);
    ctx.closePath();
    ctx.fillStyle = '#7fffd4';
    ctx.fill();
    ctx.restore();

  }

  fire() {
    const currentTime = Date.now();
    if (currentTime - this.lastFireTime > this.fireRate) {
      this.lastFireTime = currentTime;
      return new Bullet(this.x + this.size * 2, this.y + this.size, 10, 0, "#00ffff");
    }
    return null;
  }

}


